/**
 * Adds two numbers.
 *
 * @name add
 * @static
 * @public
 * @param {number} a The first number in an addition.
 * @param {number} b The second number in an addition.
 * @returns {number} Returns the total.
 * @example
 * //-
 * // Adds two numbers
 * //-
 * add(2, 3);
 * //=>
 * // 5
 * //=>
 */
function add(a, b) {
  return a + b;
}
