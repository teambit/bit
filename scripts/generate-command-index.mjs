#!/usr/bin/env node
// Codegen for:
//   - `scopes/harmony/bit/command-index.generated.ts`  (Slice 2)
//   - `scopes/harmony/core/slot-index.generated.ts` (Slot Type → Aspect ID[])
//
// The command index is built by eagerly loading Bit and snapshotting
// `cli.commandsByAspect()`. The slot index is built by static analysis of
// `*.main.runtime.ts` files because slot type parameters are erased at runtime.
//
// Run: npm run generate-command-index

import { readdirSync, readFileSync, statSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { createRequire } from 'node:module';

const HERE = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(HERE, '..');
const COMMAND_INDEX_FILE = join(REPO_ROOT, 'scopes/harmony/bit/command-index.generated.ts');
const SLOT_INDEX_FILE = join(REPO_ROOT, 'scopes/harmony/core/slot-index.generated.ts');

const SLOT_SEARCH_ROOTS = ['scopes', 'components'];
const SLOT_SKIP_DIRS = new Set([
  'node_modules',
  'dist',
  'excluded-fixtures',
  '__fixtures__',
  '__snapshots__',
  '.git',
]);

// While regenerating, the committed file may be stale or empty — don't let
// the bootstrap assertion fail mid-run.
process.env.BIT_SKIP_COMMAND_INDEX_ASSERT = '1';

const repoRequire = createRequire(join(REPO_ROOT, 'package.json'));
const { loadBit } = repoRequire('@teambit/bit');
const { CLIAspect } = repoRequire('@teambit/cli');

const harmony = await loadBit(REPO_ROOT);
const cli = harmony.get(CLIAspect.id);

const entries = [];
for (const [aspectId, commands] of cli.commandsByAspect()) {
  for (const cmd of commands) entries.push(toEntry(aspectId, cmd));
}
sortEntries(entries);

writeFileSync(COMMAND_INDEX_FILE, renderCommandIndex(entries));
console.log(`wrote ${entries.length} top-level commands to ${COMMAND_INDEX_FILE}`);

const slotIndex = buildSlotIndex();
writeFileSync(SLOT_INDEX_FILE, renderSlotIndex(slotIndex));
const totalSlotTypes = Object.keys(slotIndex).length;
const totalSlotProducers = Object.values(slotIndex).reduce((n, ids) => n + ids.length, 0);
console.log(
  `wrote ${totalSlotTypes} slot types (${totalSlotProducers} producer entries) to ${SLOT_INDEX_FILE}`,
);

process.exit(0);

// ── command-index helpers ──────────────────────────────────────────────────

function commandName(name) {
  return String(name).split(' ')[0].trim();
}

function toEntry(aspectId, cmd) {
  // Preserve the field order from the interface in command-index.generated.ts
  // — JS object insertion order is what JSON.stringify emits.
  const entry = { name: commandName(cmd.name), aspectId };
  if (cmd.alias) entry.alias = String(cmd.alias);
  if (cmd.description) entry.description = String(cmd.description);
  if (cmd.group) entry.group = String(cmd.group);
  if (cmd.private) entry.private = true;
  if (cmd.loader === false) entry.loader = false;
  if (cmd.loadAspects === false) entry.loadAspects = false;
  if (cmd.remoteOp) entry.remoteOp = true;
  if (cmd.skipWorkspace) entry.skipWorkspace = true;
  if (Array.isArray(cmd.commands) && cmd.commands.length > 0) {
    const subs = cmd.commands.map((sub) => toEntry(aspectId, sub));
    sortEntries(subs);
    entry.subCommands = subs;
  }
  return entry;
}

function sortEntries(arr) {
  arr.sort((a, b) => a.name.localeCompare(b.name) || a.aspectId.localeCompare(b.aspectId));
}

function renderCommandIndex(allEntries) {
  return `// AUTO-GENERATED — DO NOT EDIT BY HAND.
// Generated by scripts/generate-command-index.mjs
// (RFC: ESM Migration with Lazy-Loaded Aspects, Slice 2)
// To regenerate, run: npm run generate-command-index

export interface CommandIndexEntry {
  /** Command name (first word of \`Command.name\` — strips the \`<arg>\` / \`[arg]\` parts). */
  name: string;
  /** Aspect id that registered the command. */
  aspectId: string;
  alias?: string;
  description?: string;
  group?: string;
  private?: boolean;
  loader?: boolean;
  loadAspects?: boolean;
  remoteOp?: boolean;
  skipWorkspace?: boolean;
  /** Sub-command entries (same shape, inherits aspectId from parent). */
  subCommands?: CommandIndexEntry[];
}

export const COMMAND_INDEX: CommandIndexEntry[] = ${JSON.stringify(allEntries, null, 2)};
`;
}

// ── slot-index helpers ─────────────────────────────────────────────────────

function buildSlotIndex() {
  // type name → Set of aspect ids (Set keeps emission deterministic + dedupes)
  const typeToAspects = new Map();
  // aspectId cache per directory to avoid re-reading sibling .aspect.ts files
  const aspectIdCache = new Map();

  for (const root of SLOT_SEARCH_ROOTS) {
    const absRoot = join(REPO_ROOT, root);
    walkRuntimeFiles(absRoot, (filePath) => {
      const types = extractSlotTypes(filePath);
      if (types.length === 0) return;
      const aspectId = resolveAspectId(dirname(filePath), aspectIdCache);
      if (!aspectId) return;
      for (const type of types) {
        const set = typeToAspects.get(type) ?? new Set();
        set.add(aspectId);
        typeToAspects.set(type, set);
      }
    });
  }

  const result = {};
  for (const type of [...typeToAspects.keys()].sort((a, b) => a.localeCompare(b))) {
    result[type] = [...typeToAspects.get(type)].sort((a, b) => a.localeCompare(b));
  }
  return result;
}

function walkRuntimeFiles(dir, visit) {
  let entries;
  try {
    entries = readdirSync(dir, { withFileTypes: true });
  } catch {
    return;
  }
  for (const entry of entries) {
    if (entry.isSymbolicLink()) continue;
    const full = join(dir, entry.name);
    if (entry.isDirectory()) {
      if (SLOT_SKIP_DIRS.has(entry.name)) continue;
      walkRuntimeFiles(full, visit);
      continue;
    }
    if (!entry.isFile()) continue;
    if (entry.name.endsWith('.main.runtime.ts')) visit(full);
  }
}

function extractSlotTypes(filePath) {
  const src = readFileSync(filePath, 'utf8');
  const startIdx = src.search(/static\s+slots\s*=\s*\[/);
  if (startIdx === -1) return [];
  const openBracket = src.indexOf('[', startIdx);
  const closeBracket = matchBracket(src, openBracket, '[', ']');
  if (closeBracket === -1) return [];
  const block = src.slice(openBracket + 1, closeBracket);
  return extractWithTypeArgs(block);
}

// Walks a string with balanced-bracket awareness (skips strings + line comments).
function matchBracket(src, openIdx, openCh, closeCh) {
  let depth = 0;
  let i = openIdx;
  while (i < src.length) {
    const ch = src[i];
    if (ch === '/' && src[i + 1] === '/') {
      i = src.indexOf('\n', i);
      if (i === -1) return -1;
      continue;
    }
    if (ch === '/' && src[i + 1] === '*') {
      const end = src.indexOf('*/', i + 2);
      if (end === -1) return -1;
      i = end + 2;
      continue;
    }
    if (ch === '"' || ch === "'" || ch === '`') {
      i = skipString(src, i);
      continue;
    }
    if (ch === openCh) depth++;
    else if (ch === closeCh) {
      depth--;
      if (depth === 0) return i;
    }
    i++;
  }
  return -1;
}

function skipString(src, startIdx) {
  const quote = src[startIdx];
  let i = startIdx + 1;
  while (i < src.length) {
    const ch = src[i];
    if (ch === '\\') {
      i += 2;
      continue;
    }
    if (ch === quote) return i + 1;
    i++;
  }
  return src.length;
}

// Extracts the type arg from each `Slot.withType<...>()` occurrence,
// handling nested angle brackets (e.g. `EnvService<any>`).
function extractWithTypeArgs(block) {
  const types = [];
  const re = /Slot\s*\.\s*withType\s*</g;
  let match;
  while ((match = re.exec(block))) {
    const start = re.lastIndex; // index after `<`
    let depth = 1;
    let i = start;
    while (i < block.length && depth > 0) {
      const ch = block[i];
      if (ch === '<') depth++;
      else if (ch === '>') depth--;
      if (depth === 0) break;
      i++;
    }
    if (depth !== 0) continue;
    const type = block.slice(start, i).trim();
    if (type) types.push(type);
    re.lastIndex = i + 1;
  }
  return types;
}

function resolveAspectId(dir, cache) {
  if (cache.has(dir)) return cache.get(dir);
  let aspectId = null;
  let entries;
  try {
    entries = readdirSync(dir, { withFileTypes: true });
  } catch {
    cache.set(dir, null);
    return null;
  }
  for (const entry of entries) {
    if (!entry.isFile() || !entry.name.endsWith('.aspect.ts')) continue;
    const src = readFileSync(join(dir, entry.name), 'utf8');
    const m = src.match(/id:\s*['"]([^'"]+)['"]/);
    if (m) {
      aspectId = m[1];
      break;
    }
  }
  cache.set(dir, aspectId);
  return aspectId;
}

function renderSlotIndex(slotIndex) {
  return `// AUTO-GENERATED — DO NOT EDIT BY HAND.
// Generated by scripts/generate-command-index.mjs
// (RFC: ESM Migration with Lazy-Loaded Aspects — slot producer index)
// To regenerate, run: npm run generate-command-index
//
// Map of slot type-parameter name → list of aspect ids whose \`*.main.runtime.ts\`
// declares a \`static slots = [Slot.withType<TYPE>()]\` of that type. Built by
// static analysis; the type name is the literal source text inside \`<...>\`.

export const SLOT_INDEX: Record<string, string[]> = ${JSON.stringify(slotIndex, null, 2)};
`;
}
